    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
        // TODO add your handling code here:
        
        boolean flag = false;
        
        if(!jTextField1.getText().isEmpty() && !jTextField2.getText().isEmpty() &&
          !jTextField3.getText().isEmpty() && !jTextField4.getText().isEmpty() &&
           !jTextField5.getText().isEmpty()) { 
            
            
                if(validateEmail(jTextField2.getText()))
                {
                    flag = true;
                }
                
                else{
                     JOptionPane.showMessageDialog(null, "Please enter a valid email signature");
                }
                    
        }
        
        
        if(flag){
        
            int a = submitComplaint();
            if( a > 0){
                  JOptionPane.showMessageDialog(null, "Complaint registered successfully...");
            }
            else{
                  JOptionPane.showMessageDialog(null, "Unfortunately, Complaint could not be filed. Please recheck all the fields.");
            }
        
        }
        
        else{
            JOptionPane.showMessageDialog(null, "Please recheck all the fields.");
        }
    }//GEN-LAST:event_jButton1MouseClicked

